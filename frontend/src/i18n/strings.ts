export type StringKey = keyof typeof STRINGS.es;

export function t(language: AppLanguage | undefined, key: StringKey, params?: Record<string, string>): string {
  const text: string = STRINGS[normalizeLanguage(language)][key];
  if (!params) return text;
  let result: string = text;
  for (const [k, v] of Object.entries(params)) {
    result = result.replace(`{${k}}`, v);
  }
  return result;
}
